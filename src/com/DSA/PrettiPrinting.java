package com.DSA;

public class PrettiPrinting {
    public static void main(String[] args) {
        float a= 453.23543f;
        System.out.printf("Formatted float %.2f", a); //  also round of the a
        System.out.println();
        System.out.println(Math.PI);
        System.out.printf("pi : %.1f" , Math.PI);
        System.out.println();
        System.out.printf("Hello I am %s %s", "Avinash", "Suthar");
    }
}
